function theBeatlesPlay(arrayArtists, arrayInstruments) {
  var newArray = [];
  for (let i = 0; i < arrayArtists.length; i++) {
    newArray.push(`${arrayArtists[i]} plays ${arrayInstruments[i]}`)
  }
  return newArray;
}

